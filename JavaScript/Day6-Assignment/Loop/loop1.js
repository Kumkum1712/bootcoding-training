// For Loop
console.log('This is for loop');
for(let i=0; i<=10; i++){
    console.log(i);
}

// While loop
console.log('This is while loop');
let num=0;
while(num <= 10){
    console.log(num);
    num++;
}

// Do-while loop 
console.log('This is do-while loop');
let j=0;
do{
    console.log(j);
    j++;
}while(j <= 10);


